# mostra oi na tela 10 vezes

"""
Claro! No Python, o underscore (_) é frequentemente utilizado como
uma convenção para representar uma variável que não será usada.
Quando você está iterando sobre uma sequência e não precisa realmente
usar o valor do elemento atual, você pode usar _ como o nome da variável
,indicando para outras pessoas que estão lendo o código que o valor
não é relevante.
"""
for _ in range(10):
    print('oi'.title())